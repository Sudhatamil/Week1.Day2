package Week1.Day2;

public class Calculator {
	public int aDD(int x,int y,int z)
	{
		int result=x+y+z;
		return result;
	}
	public int sub(int x,int y)
	{
		int result=x-y;
		return result;
	}
	public int mul(int x,int y)
	{
		int result=x*y;
		return result;
	}
	public double div(double x,double y)
	{
		double result=x/y;
		return result;
	}
	

}
